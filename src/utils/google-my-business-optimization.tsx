// Générateur de posts automatiques
export const generateWeeklyGMBPost = (weekNumber: number) => {
  const posts = gmbOptimizationData.weeklyPosts;
  const post = posts[(weekNumber - 1) % posts.length];

  return {
    ...post,
    scheduledDate: new Date(Date.now() + (weekNumber * 7 * 24 * 60 * 60 * 1000)),
    hashtags: post.keywords.map(k => `#${k.replace(/ /g, '')}`).join(' ')
  };
};